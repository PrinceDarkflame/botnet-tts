import sys, os
from gtts import gTTS
from playsound import playsound
count = 0
while True:
    count += 1
    words = input(">> ")
    if words == "quit":
        if os.path.isfile("words" + str(count) + ".mp3") == True:
            os.remove("words" + str(count) + ".mp3")
        sys.exit()
    else:
        tts = gTTS(text=words, lang='en', slow=False)
        tts.save("words" + str(count) + ".mp3")
        playsound("words" + str(count) + ".mp3")
        os.system('cls')
        if os.path.isfile("words" + str(count) + ".mp3") == True:
            os.remove("words" + str(count) + ".mp3")

